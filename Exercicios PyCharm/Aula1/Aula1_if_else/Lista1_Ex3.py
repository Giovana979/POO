"""
3) Escreva um programa que leia dois valores
inteiros distintos e informe qual é o maior.
"""

num1 = int(input("Digite o numero 1: "))
num2 = int(input("Digite o numero 2: "))

if num1>num2:
    print("O numero 1 = ", num1, "É maior que o numero 2 = ",num2)
else:
    print("O numero 2 = ", num2, "É maior que o numero 1 = ", num1)
